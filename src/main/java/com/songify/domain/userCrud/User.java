package com.songify.domain.userCrud;

import com.songify.util.BaseEntity;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.SequenceGenerator;
import jakarta.persistence.Table;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

import java.util.Collection;
import java.util.HashSet;

@Entity
@Table(name = "users")
@Setter
@Getter
@NoArgsConstructor
public class User extends BaseEntity {



    @Id
    @GeneratedValue(generator = "users_id_seq", strategy = GenerationType.SEQUENCE)
    @SequenceGenerator(
            name = "users_id_seq",
            sequenceName = "users_id_seq",
            allocationSize = 1)
    private Long id;

    @Column(nullable = false, unique = true)
    private String email;

    private String password;

    private String confirmationToken;

    private boolean enabled = false;

    public boolean confirm(){
        this.setEnabled(true);
        this.setConfirmationToken(null);
        return true;
    }

    private Collection<String> authorities = new HashSet<>();

    public User(String email, String password, boolean enabled, Collection<String> authorities) {
        this.email = email;
        this.password = password;
        this.enabled = enabled;
        this.authorities = authorities;
    }

    public User(String email, String password, String confirmationToken, Collection<String> authorities) {
        this.email = email;
        this.password = password;
        this.confirmationToken = confirmationToken;
        this.authorities = authorities;
    }
}
